// Assignment Operators

// 1. Addition Assignment Operators (+=)

let number = 0;

number = number + 7; 
number += 3;

console.log(number); // 10

// 2. Subtraction Assignment Operator (-=)

let digit = 50;

digit -= 25;
digit = digit - 10;
console.log(digit); // 15

// 3. Multiplication Assignment Operator (*=)

let score = 10

score *= 20;
score = score * 30;
console.log(score); // 6000

// 4. Division Assignment Operator (/=)
 
let total_Bill = 100;
let no_of_friends = 7;
let Bill_per_person = total_Bill / no_of_friends;
console.log(Bill_per_person); // 14.285714285714286 
